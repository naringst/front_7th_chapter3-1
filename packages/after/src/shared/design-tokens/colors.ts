/**
 * 색상 토큰 이름을 생성하는 헬퍼 함수
 */
export function getColorToken(
  category: keyof typeof colorTokens,
  value: string | number,
): string {
  if (category === 'opacity') {
    return `color-atomic-opacity-${value}`;
  }
  return `color-atomic-${category}-${value}`;
}

/**
 * CSS 변수 이름을 생성하는 헬퍼 함수
 */
export function getColorVariable(
  category: keyof typeof colorTokens,
  value: string | number,
): string {
  return `--${getColorToken(category, value)}`;
}
